    private void teclaPresionada(java.awt.event.KeyEvent evt) {
        char tecla = evt.getKeyChar();

        if (Character.isDigit(tecla) || tecla == '.') {
            // Si es un d�gito, actualizar el texto en la pantalla
            if (this.limpiarTexto) {
                this.txtResultado.setText(Character.toString(tecla));
                this.limpiarTexto = false;
            } else {
                this.txtResultado.setText(this.txtResultado.getText() + tecla);
            }
            this.sePuedeCalcular = true;
        } else {
            // Si es un operador, realizar la operaci�n correspondiente
            switch (tecla) {
                case '+':
                    btnPlusActionPerformed(null);
                    break;
                case '-':
                    btnMinusActionPerformed(null);
                    break;
                case '*':
                    btnMultActionPerformed(null);
                    break;
                case '/':
                    btnDivActionPerformed(null);
                    break;
                case '\n':
                    btnEqualActionPerformed(null);
                    break;
                case '\b':
                    // Si es la tecla de retroceso, borrar un car�cter
                    String textoActual = this.txtResultado.getText();
                    if (!textoActual.isEmpty()) {
                        this.txtResultado.setText(textoActual.substring(0, textoActual.length() - 1));
                    }
                    break;
                case 'c':
                case 'C':
                    // Si es 'C' (may�scula o min�scula), limpiar la pantalla
                    btnClearActionPerformed(null);
                    break;
                case '\u007F':
                    btnClearAllActionPerformed(null);
                    break;
                default:
                    break;
            }
        }
    }

