    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualActionPerformed
        if (this.sePuedeCalcular) {
            double operandoActual = Double.parseDouble(this.operandoActual);
            if (this.primerOperando) {
                resultado = operandoActual;
                this.primerOperando = false;
            } else {
                switch (this.ultimoOperando) {
                    case '+':
                        resultado += operandoActual;
                        break;
                    case '-':
                        resultado -= operandoActual;
                        break;
                    case '*':
                        resultado *= operandoActual;
                        break;
                    case '/':
                        if (operandoActual == 0) {
                            JOptionPane.showMessageDialog(this, "No se puede dividir entre 0");
                        } else {
                            resultado /= operandoActual;
                        }
                        break;
                }
            }
            this.operandoActual = String.valueOf(this.resultado);
                    this.txtResultado.setText(String.format("%.2f", this.resultado));
            this.txtResultado.setText(this.resultado + "");
            this.limpiarTexto = true;
            this.sePuedeCalcular = false;
        }
    }//GEN-LAST:event_btnEqualActionPerformed

